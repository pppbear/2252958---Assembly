CODESEG SEGMENT
	ASSUME CS:CODESEG,DS:DATASEG
MAIN PROC FAR
    MOV AX, DATASEG      ; 初始化数据段
    MOV DS, AX
	MOV AH,1             ; 输入数字
	MOV CX,3
	; 检查输入是否合理
L1: 
    CMP AL, '1'
	INT 21H
	CMP AL, 0DH          ; 输入回车直接结束
	JE DONE   
	LOOP L1

DONE:
    MOV AX,4C00H
	INT 21H

MAIN ENDP
CODESEG ENDS
	END MAIN
